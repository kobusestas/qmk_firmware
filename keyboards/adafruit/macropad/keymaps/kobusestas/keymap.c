bool process_record_user(uint16_t keycode, keyrecord_t* record) {
  switch (keycode) {
    case EMAIL:  
      if (record->event.pressed) {
        SEND_STRING("justinkobus@geico.com");
      }
      return false;
    case CSACCOUNT:  
      if (record->event.pressed) {
        SEND_STRING("A278092CS@geico.com");
      }
      return false;
    case ACCOUNTNAME:  
      if (record->event.pressed) {
        SEND_STRING("A278092");
      }
      return false;
  }
  return true;
}

